package collaborate2_14;

public class Division {

	public static void main(String[] args) {
		
		
		int x = 105;
		int y = x/60;    // 34  integer division
		int z = x%60;	// 2 remainder  "mod"
		
		System.out.println(x + " number of seconds is really " + y + 
				" minutes and " + z + " seconds");
	}

}
